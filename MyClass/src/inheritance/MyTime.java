package inheritance;

public class MyTime extends MyDate {

	int hh,mmm,ss;

	public int getHh() {
		return hh;
	}

	public void setHh(int hh) {
		this.hh = hh;
	}

	public int getMmm() {
		return mmm;
	}

	public void setMmm(int mmm) {
		this.mmm = mmm;
	}

	public int getSs() {
		return ss;
	}

	public void setSs(int ss) {
		this.ss = ss;
	}
	
	
}
